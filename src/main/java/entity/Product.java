package entity;

public class Product {
    private int id;
    private String productName;
    private int quanntiy;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public int getQuanntiy() {
        return quanntiy;
    }

    public void setQuanntiy(int quanntiy) {
        this.quanntiy = quanntiy;
    }
}
